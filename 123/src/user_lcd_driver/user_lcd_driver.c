void SPI_SendByte(unsigned  char byte)              //向液晶屏写一个8位数据
{

  unsigned char counter;

  for(counter=0;counter<8;counter++)
  {
    SPI_SCK_0;
    if((byte&0x80)==0)
    {
      SPI_SDA_0;
    }
    else SPI_SDA_1;
    byte=byte<<1;
    SPI_SCK_1;
  }
    SPI_SCK_0;
}

void SPI_SendByte_16(unsigned  int byte)              //向液晶屏写一个16位数据
{

  unsigned char counter;

  for(counter=0;counter<16;counter++)
  {
    SPI_SCK_0;
    if((byte&0x8000)==0)
    {
      SPI_SDA_0;
    }
    else SPI_SDA_1;
    byte=byte<<1;
    SPI_SCK_1;
  }
    SPI_SCK_0;
}

void TFT_SEND_CMD(unsigned char o_command)
  {
    SPI_DC_0;
    SPI_CS_0;

    SPI_SendByte(o_command);
      SPI_CS_1;
  }
  //向液晶屏写一个8位数据
void TFT_SEND_DATA(unsigned  char o_data)
  {
    SPI_DC_1;
    SPI_CS_0;
    SPI_SendByte(o_data);
    SPI_CS_1;
   }
void TFT_SEND_DATA_16(unsigned int o_data)
{
    SPI_DC_1;
    SPI_CS_0;
    SPI_SendByte_16(o_data);
    SPI_CS_1;
}
